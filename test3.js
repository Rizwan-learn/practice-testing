class calculator {
    add(a,b){
        let result = a+b;
        return result;
    }
    minus(a,b){
        let result = a-b;
        return result;
    }
    multiply(a,b){
        let result = a*b;
        return result;
    }
    div(a,b){
        let result = a/b;
        return result;
    }
}

module.exports = calculator;